
function isEven(number) {
    if (number % 2 === 0) {
        return true;
    } else {
        return false;
    }
}
console.log(isEven(13));
console.log(isEven(12));

function isOdd(number) {
    if (number % 2 === 1) {
        return true;
    } else {
        return false;
    }
}
console.log(isOdd(30));
console.log(isOdd(31));